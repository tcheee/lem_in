#include <stdlib.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strcpy(char *dst, char const *src)
{
	int i;

	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}

char	*ft_strdup(char *src)
{
	int len;
	char *dup;

	len = ft_strlen(src);
	dup = malloc(sizeof(char) * (len + 1));
	if (src == NULL)
		return (0);
	ft_strcpy(dup, src);
	return (dup);
}
